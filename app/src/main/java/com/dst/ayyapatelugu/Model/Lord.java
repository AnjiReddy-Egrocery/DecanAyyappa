package com.dst.ayyapatelugu.Model;

public class Lord {
    private int id;
    private String name;
    private String vedic_name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getVedic_name() {
        return vedic_name;
    }

    public void setVedic_name(String vedic_name) {
        this.vedic_name = vedic_name;
    }
}
